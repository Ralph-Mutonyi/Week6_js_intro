// Arrays Methods

// Push

let myArray = ["Monday", "Tuesday","Wednesday"];

console.log(myArray);

myArray.push("Thursday");

console.log(myArray);


// pop methods

myArray.pop("Thursday");
console.log(myArray);


myArray.pop(2);
console.log(myArray);

// unshift

myArray.unshift("Sunday");
console.log(myArray);

// shift

myArray.shift(0);

console.log(myArray);
