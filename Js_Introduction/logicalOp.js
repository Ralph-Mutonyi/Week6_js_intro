var x =100;
var y = 50;

// AND - && - returns true if both conditions are true

if(x == 100 && y ==50){
    console.log (true);
}else{
    console.log (false);
}
// OR - || - returns true of one condition is true
if(x == 100 || y ==50){
    console.log (true);
}else{
    console.log (false);
}

// NOT - != returns the opposite of the condition that is met
if(x == 100 != y ==50){
    console.log (true);
}else{
    console.log (false);
}

